func Test_groupAnagrams(t *testing.T) {
	sortSlice := func(slice [][]string) [][]string {
		for i := 0; i < len(slice); i++ {
			sort.Slice(slice[i], func(a, b int) bool {
				return slice[i][a] < slice[i][b]
			})
		}
		sort.Slice(slice, func(i, j int) bool { return slice[i][0] < slice[j][0] })
		return slice
	}

	type args struct {
		strs []string
	}
	tests := []struct {
		name string
		args args
		want [][]string
	}{
		// TODO: Add test cases.
		{args: args{strs: []string{"eat", "tea", "tan", "ate", "nat", "bat"}}, want: [][]string{{"ate", "eat", "tea"}, {"nat", "tan"}, {"bat"}}},
	}
	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			if got := groupAnagrams(tt.args.strs); !reflect.DeepEqual(sortSlice(got), sortSlice(tt.want)) {
				t.Errorf("groupAnagrams() = %v, want %v", got, tt.want)
			}
		})
	}
}

